void WaitBisyFlag1 () {
  uint8_t tmp = DataPort;
  DataPort = 0;
  DDRDataPort = 0x00;
  Ctrl = (1<<RST) | (0 << RS) |
  (1 << RW) | (1 << CS1) | (0 << CS2) | (1 << E);
  while (PINDataPort != 0) {

  }

  Ctrl = (1<<RST) | (0 << RS) |
  (1 << RW) | (0 << CS1) | (1 << CS2) | (1 << E);
  while (PINDataPort != 0) {

  }
  Ctrl = (1<<RST) | (0 << RS) |
	  (0 << RW) | (0 << CS1) | (0 << CS2) | (0 << E);
  DataPort = tmp;
  DDRDataPort = 0xFF;
}

// Reset function for LCD, using !RST pin
void LCDReset () {

	Ctrl |= 0 << RST;
	WaitBisyFlag1 ();
	Ctrl |= 1 << RST;
	WaitBisyFlag1 ();
}

// Function wich enables or disables image on LCD display
void LCDOnOff (unsigned char State)	{

	WaitBisyFlag1 ();
	Ctrl = (1<<RST) | (0 << RS) |
	(0 << RW) | (1 << CS1) | (1 << CS2) | (0 << E);
	DataPort = (0b11111 << 1) | (State<<0);
	Ctrl |= (1 << E);
	_delay_us (10);
	Ctrl &= ~(1 << E);
	WaitBisyFlag1 ();
}



// Function wich sets number of page for LCD
void LCDSetPage (uint8_t Page) {

// Page value is 3-bit, range checking
	if (Page > 7) {
		Page &= ~(0b111);
	}

	WaitBisyFlag1 ();
	Ctrl = (1<<RST) | (0 << RS) |
	(0 << RW)
	| (1 << CS1) | (1 << CS2) | (0 << E);
	DataPort = (0b10111 << 3) | (Page);
	Ctrl |= (1 << E);
	_delay_us (10);
	Ctrl &= ~(1 << E);
	WaitBisyFlag1 ();
}


void LCDSetColumn (uint8_t Column) {

	WaitBisyFlag1 ();
	Ctrl = (1<<RST) | (0 << RS) |
	(0 << RW) | (1 << CS1) | (1 << CS2) | (0 << E);
	DataPort = (0b01 << 6) | (Column);
	Ctrl |= (1 << E);
	_delay_us (10);
	Ctrl &= ~(1 << E);
	WaitBisyFlag1 ();
}

void LCDWriteData (uint8_t Data, uint8_t X, uint8_t Y) {
	uint8_t cs1mask = 0;
	uint8_t cs2mask = 0;
	if (X > 63 && X < 128) cs2mask = 1;
	else  cs1mask = 1;

	LCDSetColumn (X);
	LCDSetPage (Y);
	WaitBisyFlag1 ();
	Ctrl = (1<<RST) | (1 << RS) |
	(0 << RW) | (0 << E) | (cs1mask <<CS1) | (cs2mask <<CS2);
	DataPort = Data;
	Ctrl |= (1 << E);
	_delay_us (10);
	Ctrl &= ~(1 << E);
	WaitBisyFlag1 ();
}

void LCDWriteDataDual (uint8_t Data) {

	WaitBisyFlag1 ();
	Ctrl = (1<<RST) | (1 << RS) |
	(0 << RW) | (0 << E) | (1<<CS1) | (1 <<CS2);
	DataPort = Data;
	Ctrl |= (1 << E);
	_delay_us (10);
	Ctrl &= ~(1 << E);
	WaitBisyFlag1 ();
}


void LCDClear () {
  LCDOnOff (off);
  for (uint8_t j = 0; j < 8; j++) {
	LCDSetPage (j);
	for (uint8_t i = 0; i < 64; i++) {
      LCDSetColumn (i);
	  LCDWriteDataDual (0);
    }
  }
  LCDOnOff (on);
}

